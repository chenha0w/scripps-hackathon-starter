def zero_shot_annotation(adata, marker_genes):
    cluster_annotations = {}
    for cluster in sorted(adata.obs["leiden"].unique(), key=int):
        cluster_mask = adata.obs["leiden"] == cluster
        cluster_data = adata[cluster_mask]
        scores = {}
        for cell_type, genes in marker_genes.items():
            valid_genes = [g for g in genes if g in adata.var_names]
            if valid_genes:
                X = cluster_data[:, valid_genes].X
                avg_expr = (X.toarray() if issparse(X) else X).mean()
                scores[cell_type] = float(avg_expr)
        cluster_annotations[cluster] = max(scores, key=scores.get)
    return cluster_annotations

cluster_annotations = zero_shot_annotation(adata, MARKER_GENES)
print("Zero-shot cluster annotations:")
for c, ct in sorted(cluster_annotations.items(), key=lambda x: int(x[0])):
    print(f"  Cluster {c}: {ct}")

# Expand cluster → cell level
adata.obs["zero_shot_annotation"] = adata.obs["leiden"].map(cluster_annotations)
