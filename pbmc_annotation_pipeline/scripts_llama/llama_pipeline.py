def safe_map(series, mapping):
    """Map a pandas Series (possibly Categorical) using a dict, returning a plain str Series."""
    return pd.Series(
        [mapping.get(str(v), "Unknown") for v in series],
        index=series.index, dtype=str
    )

adata.obs["gt_canonical"]  = safe_map(adata.obs["ground_truth"],         GT_MAP)
adata.obs["zs_canonical"]  = safe_map(adata.obs["zero_shot_annotation"],  ZS_MAP)
adata.obs["ref_canonical"] = safe_map(adata.obs["reference_annotation"],  REF_MAP)

# ── Metrics (Llama: "calculate accuracy, precision, recall, F1, confusion matrix") ──
def calc_metrics(pred_col, label):
    df = adata.obs[["gt_canonical", pred_col]].copy()
    df = df[(df["gt_canonical"] != "Unknown") & (df[pred_col] != "Unknown")]
    y_t, y_p = df["gt_canonical"].tolist(), df[pred_col].tolist()
    n = len(y_t)
    acc = accuracy_score(y_t, y_p)
    wf1 = f1_score(y_t, y_p, average="weighted", zero_division=0)
    mf1 = f1_score(y_t, y_p, average="macro",    zero_division=0)
    kap = cohen_kappa_score(y_t, y_p)
    rep = classification_report(y_t, y_p, output_dict=True, zero_division=0)
    per = (pd.DataFrame(rep).T.iloc[:-3]
           .rename(columns={"support":"n_cells"})
           .assign(n_cells=lambda d: d["n_cells"].astype(int)))
    print(f"\n── {label} ──")
    print(f"  Cells : {n}/{len(adata)}  ({100*n/len(adata):.1f}%)")
    print(f"  Accuracy    : {acc:.4f}  ({100*acc:.1f}%)")
    print(f"  Macro F1    : {mf1:.4f}")
    print(f"  Weighted F1 : {wf1:.4f}")
    print(f"  Kappa       : {kap:.4f}")
    print(per[["precision","recall","f1-score","n_cells"]].to_string())
    return dict(method=label, n_cells=n, accuracy=acc,
                macro_f1=mf1, weighted_f1=wf1, cohen_kappa=kap,
                per_class=per, y_true=y_t, y_pred=y_p)

print("\n── Evaluation ──")
