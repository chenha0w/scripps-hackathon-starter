# ── Method 1: Zero-Shot Annotation (Llama's approach) ─────────────────────────
# Llama: "Calculate the average expression of marker genes for each cell,
#          assign cells to the cell type with the highest average expression."
# Fixed: apply per cluster (not per cell returning gene index).
print("\n── Method 1: Zero-Shot Annotation (Llama) ──")
t0_zs = time.time()
