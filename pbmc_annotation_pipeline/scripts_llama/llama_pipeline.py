def reference_mapping(adata, reference_adata, ref_label_col="bulk_labels"):
    """Llama's dot-product similarity reference mapping."""
    # Find common genes
    common_genes = adata.var_names.intersection(reference_adata.var_names)
    print(f"  Common genes: {len(common_genes)}")

    # Compute reference centroids (average expression per cell type)
    ref_sub = reference_adata[:, common_genes]
    cell_type_centroids = {}
    for cell_type in ref_sub.obs[ref_label_col].unique():
        mask = ref_sub.obs[ref_label_col] == cell_type
        X = ref_sub[mask].X
        centroid = np.array(X.toarray() if issparse(X) else X).mean(axis=0)
        cell_type_centroids[cell_type] = centroid

    # For each test cell, compute dot-product similarity with each centroid
    test_sub = adata[:, common_genes]
    annotations = []
    batch = 200
    for start in range(0, test_sub.n_obs, batch):
        X_batch = test_sub[start:start+batch].X
        if issparse(X_batch):
            X_batch = X_batch.toarray()
        for cell_vec in X_batch:
            sims = {ct: float(np.dot(cell_vec, centroid))
                    for ct, centroid in cell_type_centroids.items()}
            annotations.append(max(sims, key=sims.get))
    return annotations

